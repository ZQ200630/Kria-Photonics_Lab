def write_summary(event_dir: Path, out_dir: Path, metadata: dict[str, Any], generated: Iterable[Path]) -> Path:
    path = out_dir / "summary.txt"
    lines = [
        f"recording_dir: {event_dir}",
        f"run_name: {metadata.get('run_name', '')}",
        f"event_name: {metadata.get('event_name', '')}",
        f"lock_method: {metadata.get('lock_method', '')}",
        f"selected_code: {metadata.get('selected_code', '')}",
        f"actual_code: {metadata.get('actual_code', '')}",
        f"selected_reference_index: {metadata.get('selected_reference_index', '')}",
        f"actual_reference_index: {metadata.get('actual_reference_index', '')}",
        "",
        "generated_plots:",
    ]
    lines.extend(f"  {item.name}" for item in generated)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return path


def plot_recording(event_dir: Path, output: str | None = None) -> list[Path]:
    event_dir = event_dir.expanduser().resolve()
    if not event_dir.exists():
        raise FileNotFoundError(event_dir)
    out_dir = resolve_output_dir(event_dir, output)
    out_dir.mkdir(parents=True, exist_ok=True)
    metadata = read_json(event_dir / "metadata.json")
    plt = setup_matplotlib()

    if plt is not None:
        generated = [
            plot_spectrum_overlay(plt, event_dir, out_dir, metadata),
            plot_pd_temperature(plt, event_dir, out_dir),
            plot_lock_status(plt, event_dir, out_dir),
        ]
        paths = [path for path in generated if path is not None]
    else:
        paths = []
        paths.extend(plot_spectrum_overlay_svg(event_dir, out_dir, metadata))
        paths.extend(plot_pd_temperature_svg(event_dir, out_dir))
        paths.extend(plot_lock_status_svg(event_dir, out_dir))
    paths.append(write_summary(event_dir, out_dir, metadata, paths))
    return paths


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Replot Butterfly Laser lock recording bundles.")
    parser.add_argument("path", help="Recording event directory, run directory, or Butterfly_Laser_Data directory")
    parser.add_argument("--output", help="Output directory. Defaults to <event>/plots for one event.")
    parser.add_argument("--recursive", action="store_true", help="Find all metadata.json files below path and plot each event.")
    args = parser.parse_args(argv)

    root = Path(args.path)
    event_dirs = discover_recording_dirs(root) if args.recursive or not (root / "metadata.json").exists() else [root.expanduser().resolve()]
    if not event_dirs:
        print(f"No recording directories found under {root}", file=sys.stderr)
        return 1

    for event_dir in event_dirs:
        output = args.output
        if args.output and len(event_dirs) > 1:
            output = str(Path(args.output).expanduser().resolve() / event_dir.name)
        generated = plot_recording(event_dir, output)
        print(f"{event_dir}:")
        for path in generated:
            print(f"  wrote {path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
